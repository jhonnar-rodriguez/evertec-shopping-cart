$factory->define(User::class, function (Faker $faker) {
    return [
        'first_name' => $faker->name,
        'last_name' => $faker->name,
        'email' => $faker->unique()->safeEmail,
        'phone_number' => $faker->numberBetween(10000000, 2000000000000),
        'active' => true,
    ];
});
